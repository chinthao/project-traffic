let currentPart = '';

// ຟັງຊັນເລີ່ມເຮັດຄຳຖາມ
function startQuiz(partKey) {
    currentPart = partKey;
    const part = quizData[partKey];
    
    // ຊ່ອນໜ້າອື່ນ ແລະ ເປີດໜ້າຄຳຖາມ
    document.getElementById('home-section').style.display = 'none';
    document.getElementById('result-section').style.display = 'none';
    document.getElementById('quiz-section').style.display = 'block';
    
    document.getElementById('part-title').innerText = part.title;
    
    const container = document.getElementById('questions-container');
    container.innerHTML = ''; // ລ້າງຂໍ້ມູນເກົ່າ

    // ວົນລູບສ້າງ HTML ຂອງແຕ່ລະຂໍ້
    part.questions.forEach((q, index) => {
        let questionHtml = `
            <div class="question-item">
                <div class="question-text">${index + 1}. ${q.question}</div>
        `;
        
        // ຖ້າມີຮູບພາບ ໃຫ້ສະແດງ
        if(q.image) {
            questionHtml += `<img src="${q.image}" alt="ຮູບພາບປະກອບ" class="question-image" style="max-width:100%; height:auto; margin: 10px 0; display:block; border-radius:5px;">`;
        }

        questionHtml += `<ul class="options-list" style="list-style-type:none; padding-left:0;">`;
        
        // ສະແດງ Choice (ກ, ຂ, ຄ, ງ) ໂດຍອີງຕາມ Key (a, b, c, d)
        for (const [key, value] of Object.entries(q.options)) {
            let prefix = key === 'a' ? 'ກ' : key === 'b' ? 'ຂ' : key === 'c' ? 'ຄ' : 'ງ';
            questionHtml += `
                <li class="option-item" style="margin-bottom: 8px;">
                    <label class="option-label" style="cursor:pointer; display:flex; align-items:center;">
                        <input type="radio" name="question-${q.id}" value="${key}" required style="margin-right:8px;">
                        <span>${prefix}. ${value}</span>
                    </label>
                </li>
            `;
        }

        questionHtml += `</ul></div><hr style="border:0; border-top:1px solid #eee; margin:20px 0;">`;
        container.innerHTML += questionHtml;
    });
}

// ຟັງຊັນກັບຄືນໜ້າຫຼັກ
function goHome() {
    document.getElementById('quiz-section').style.display = 'none';
    document.getElementById('result-section').style.display = 'none';
    document.getElementById('home-section').style.display = 'block';
    document.getElementById('quiz-form').reset();
}

// ຟັງຊັນເລີ່ມເຮັດຄຳຖາມພາກສ່ວນເດີມໃໝ່
function retryQuiz() {
    startQuiz(currentPart);
}

// ລະບົບປະມວນຜົນຕອນກົດສົ່ງຄຳຕອບ
document.getElementById('quiz-form').addEventListener('submit', function(e) {
    e.preventDefault();
    
    const part = quizData[currentPart];
    let correctCount = 0;
    let wrongCount = 0;

    part.questions.forEach(q => {
        const selected = document.querySelector(`input[name="question-${q.id}"]:checked`);
        if (selected && selected.value === q.correct) {
            correctCount++;
        } else {
            wrongCount++;
        }
    });

    // ສະແດງໜ້າສະຫຼຸບຜົນ
    document.getElementById('quiz-section').style.display = 'none';
    document.getElementById('result-section').style.display = 'block';
    
    document.getElementById('result-part-name').innerText = part.title;
    document.getElementById('correct-score').innerText = correctCount;
    document.getElementById('wrong-score').innerText = wrongCount;
});